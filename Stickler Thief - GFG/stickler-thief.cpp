class Solution
{
    public:
    //Function to find the maximum money the thief can get.
    int FindMaxSum(int arr[], int n)
    {
        vector<int> dp(n+1,0);
        for(int ind=n-1;ind>=0;ind--)
        {
            int nottake=dp[ind+1];
            int take=arr[ind]+(ind+2<=n?dp[ind+2]:0);
            dp[ind]=max(take,nottake);
        }
        return dp[0];
    }
};

